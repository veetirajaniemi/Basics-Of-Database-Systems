-- CREATING TABLES -----------------------------------------------------------------------------------------

CREATE TABLE Team(
	team_name VARCHAR(50) NOT NULL PRIMARY KEY,
	num_of_players INTEGER,
	matches_played INTEGER,
	avg_age REAL,
	avg_possession REAL CHECK (avg_possession < 100)
);

CREATE TABLE Player(
	playerID INTEGER NOT NULL PRIMARY KEY,
	player_name VARCHAR(100),
    team_name VARCHAR(50) NOT NULL,
    age INTEGER NOT NULL,
	nationality CHAR(3),
	position CHAR(2),
    FOREIGN KEY(team_name) REFERENCES Team(team_name)
);


CREATE TABLE GK_stats(
	playerID INTEGER NOT NULL PRIMARY KEY,
	player_name VARCHAR(100) NOT NULL,
	team_name VARCHAR(50) NOT NULL,
	age INTEGER NOT NULL,
	nationality CHAR(3),
	matches_played INTEGER DEFAULT 0,
	clean_sheets INTEGER,
	goals_against INTEGER,
	FOREIGN KEY(playerID) REFERENCES Player(playerID) ON DELETE CASCADE,
	FOREIGN KEY(team_name) REFERENCES Team(team_name) ON DELETE CASCADE
);

CREATE TABLE Match_results(
	matchID INTEGER NOT NULL PRIMARY KEY,
	day	CHAR(3),
	date CHAR(10),
    home_team VARCHAR(50) NOT NULL,
	score VARCHAR(10),
    away_team VARCHAR(50) NOT NULL,
	FOREIGN KEY(home_team) REFERENCES Team(team_name) ON UPDATE CASCADE,
	FOREIGN KEY(away_team) REFERENCES Team(team_name) ON UPDATE CASCADE
);

CREATE TABLE Shooting_stats(
	playerID INTEGER NOT NULL PRIMARY KEY,
    player_name VARCHAR(100),
    team_name VARCHAR(50),
	age INTEGER NOT NULL,
	nationality CHAR(3),
	goals_scored INTEGER DEFAULT 0,
	total_shots INTEGER DEFAULT 0,
	shots_on_target INTEGER,
	FOREIGN KEY(playerID) REFERENCES Player(playerID) ON DELETE CASCADE,
	FOREIGN KEY(team_name) REFERENCES Team(team_name) ON UPDATE CASCADE
);

CREATE TABLE Wage(
	team_name VARCHAR(50) PRIMARY KEY,
	rank INTEGER UNIQUE CHECK (rank < 21),
	weekly_wages_eur INTEGER,
	contract_players INTEGER,
	FOREIGN KEY(team_name) REFERENCES Team(team_name) ON UPDATE CASCADE
);

CREATE TABLE Team_Match(
    team_matchID INTEGER NOT NULL PRIMARY KEY,
    team_name VARCHAR(50) NOT NULL,
    matchID INTEGER NOT NULL,
    FOREIGN KEY(team_name) REFERENCES Team(team_name) ON UPDATE CASCADE,
    FOREIGN KEY(matchID) REFERENCES Match_results(matchID) ON UPDATE CASCADE
);


