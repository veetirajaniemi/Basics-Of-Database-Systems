-- VIEWS ----------------------------------------------------------------------------------------------------------------------------------

	
-- View 1: Players from one team, in this example Brighton
CREATE VIEW TeamPlayersView AS
SELECT * FROM Player WHERE team_name = "Brighton";

-- View 2: Team stats - normal stats and wage stats combined
CREATE VIEW TeamView AS
SELECT team_name, matches_played, avg_age, avg_possession, num_of_players, contract_players, rank 
AS "wage_rank", weekly_wages_eur FROM 
(SELECT * FROM Team INNER JOIN Wage ON Team.team_name = Wage.team_name) T;

-- View 3: A player's special stats, searched with playerID
CREATE VIEW PlayerView AS
SELECT * FROM Shooting_stats WHERE playerID = 123;

-- View 4: All match results combined with team stats, ordered by team name
CREATE VIEW TeamMatchView AS
SELECT Team.team_name, Team.num_of_players, Team.matches_played, Team.avg_age, Team.avg_possession, Match_results.matchID,
Match_results.day, Match_results.date, Match_results.home_team, Match_results.score, Match_results.away_team FROM Team
INNER JOIN Team_Match ON Team.team_name = Team_Match.team_name
INNER JOIN Match_results ON Team_Match.matchID = Match_results.matchID
ORDER BY Team.team_name;

-- View 5: Top 10 goal scorers
CREATE VIEW TopScorerView AS
SELECT player_name AS player, team_name AS team, goals_scored FROM Shooting_stats ORDER BY goals_scored DESC LIMIT 10;


